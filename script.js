// Output teks "Angka dengan angka"
console.log("Angka dengan angka\n");

// Variabel angka
let x = 10;
let y = 2;

// Menampilkan nilai dari x dan y
console.log("x =", x,"\n");
console.log("y =", y, "\n\n\n");

// Operasi penjumlahan
console.log("Penjumlahan\n");
console.log(x, "+", y, " = ", x + y, "\n");

// Operasi pengurangan
console.log("Pengurangan\n");
console.log(x, "-", y, " = ", x - y, "\n");

// Operasi perkalian
console.log("Perkalian\n");
console.log(x, "*", y, " = ", x * y, "\n");

// Operasi eksponen (pangkat)
console.log("Eksponen\n");
console.log(x, "**", y, " = ", x ** y, "\n");

// Operasi pembagian
console.log("Pembagian\n");
console.log(x, ":", y, " = ", x / y, "\n");

// Operasi modulus
console.log("Modulus\n");
console.log(x, "%", y, " = ", x % y, "\n");

// Operasi increment (penambahan 1)
console.log("Increment\n");
console.log("++x = ", ++x,"\n");

// Operasi decrement (pengurangan 1)
console.log("Decrement\n");
console.log("--y = ", --y, "\n\n\n");

// Output teks "Teks dengan teks"
console.log("Teks dengan teks\n");

